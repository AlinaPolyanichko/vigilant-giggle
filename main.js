document.addEventListener("keydown", function(piano) {
    let keys  = ['A', 'S', 'D', 'F', 'G', 'H', 'J'];
    let input = piano.key.toUpperCase();

    if (keys.includes(input)) {
        let audio = document.createElement("AUDIO");
    }
});
